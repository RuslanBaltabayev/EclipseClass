package day6;

public class ComparisionOperation {

	public static void main(String[] args) {
		
		int a = 11, b=11;
		
		System.out.println("a and b are equal :"+ (a==b));
		System.out.println("a and b are not equal :"+ (a!=b));
		System.out.println("a is greater or equal to b :"+ (a>=b));
		System.out.println("a is less or equal to b :"+ (a<=b));
		
		

	}

}
